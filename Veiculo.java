public class Veiculo {
    String placa;
    String marca;
    String modelo;
    int ano;
    double valor;

    public Veiculo(String placa, String marca, String modelo, int ano, double valor){
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.ano = ano;
        this.valor = valor;
    }
}
